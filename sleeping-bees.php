      <div class="paragraph">
        <p>Notifoo’s second component is the Hot List, which houses vital apps and contacts whose notifications need to be delivered in real time. is might include calendar apps, reminder apps, and contacts for family members or coworkers. It is extremely easy to add and remove things from the Hot List in only a few taps, so users can adjust their settings as their needs change during the day. Upon installation, the Hot List is the default location for all notifications until organized into Lists, so that nothing accidentally gets lost. Explore the prototype here.</p>
