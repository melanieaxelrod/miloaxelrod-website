        <h2>The Website</h2>
        <p>At this point in the project, with both sides of the equation decently outlined, I began working on the website, where the final product would live. I considered using a service like Squarespace to host the site and make it easy to build, but I decided in the end that if I was creating a project about interfaces, I wanted to have maximum control of the interface through which the viewer would experience the project. So, I began building the website from scratch. </p>
