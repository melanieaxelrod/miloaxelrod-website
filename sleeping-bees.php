      <h1>The Process</h1>

      <div class="paragraph">
        <h2>Original Concept</h2>
        <p>How can design be used to address the hidden negative mental impacts of mobile technology for young people who have grown up in a world of perpetually available distraction? This constant stream of distraction discourages solitude and introspection, which leads to a variety of negative impacts on health and development. They make it incredibly enticing to waste time and avoid mental responsibility because of the instantaneous, yet empty, gratification they provide. They promote a culture of reactive response instead of critical thought because of the fast paced hive-mind culture that has evolved with social networking. They distract from consciously choosing who and what a person surrounds themselves with by encouraging automatic responses to boredom like opening Facebook and mindlessly scrolling the feed, which occupies brainpower without expanding it.</p>
        <p>Overall, they demote introspection and self-improvement by creating the habit of filling any and all empty time with mindless activity. One possible solution to this problem is to simply create awareness of the problem and the benefits of solitude. This could be done with an awareness campaign or including technology abuse as a section in public school health classes. Another route is to attack the problem directly by developing some sort of product that streamlines the notification process and promotes healthier mobile technology habits.</p>
      </div>

      <div class="paragraph">
        <h2>Refining the Problem</h2>
        <p>After continued research, it seems that the most basic problem here comes from cellphone notifications. Because they are emotionally rewarding [backed by research] they train us to want them and wait for them and even check for them when they haven’t shown up yet [verified by survey]. They are emotionally rewarding ("somebody is paying attention to me"), spontaneous & unpredictable (encourages a “waiting” mentality), and carry social weight (users feel the need to reply quickly so the other person doesn’t have to wait.)</p>
      </div>

      <div class="paragraph">
        <h2>Exploration</h2>
        <p>Through constant research, exploring of existing material, and conversation, I went on to explore the ideas surrounding my problem, from theoretical to practical and the rational to the bizarre. I found new angles and aspects that I wasn't aware of at the beginning of the project, and my concept expanded from simply finding one or two solutions, to creating a framework that allowed me to gain a deeper understanding of the systems at work.</p>
        <p>I started by thinking about notifications themselves as the sole way that your smartphone communicates with you when you don’t have it open. I brainstormed a list of as many creative notifications I could think of (some of them require custom cases or add-ons), what sense they interact with, and what kind of feeling the invoke. Unfortunately, most of them were “fear” but that’s beside the point.</p>
      </div>

