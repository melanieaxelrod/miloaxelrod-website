      <div class="paragraph">
        <h2>Solution 1: Notifoo</h2>
        <p>I began delving into realistic improvements to the current notification interface. At this stage in the project, I believed that a single, highly fleshed out design of one such solution would be my final product, and this initial creation was Notifoo.</p>
      </div>

      <div class="image-cont">
        <div class="image-1">
          <img src="img/projects/sleeping-bees/behancemockup-07.png" />
        </div>
      </div>

      <div class="paragraph">
        <p>Notifoo aims to address the spontaneity of the notification, the most pronounced of its flaws. Very important notifications certainly need to happen in real time, but a huge amount of social media and game notifications have no time sensitivity. With Notifoo, users are able to categorize these notifications into a more customizable delivery system that removes much of the guesswork of receiving a notification on the user's end. In this system, notifications from specifically selected apps and contacts would be delivered in real time, while all others would be stored in related lists that are scheduled to deliver chunks of notifications at convenient and expected times. No more will we find ourselves distracted by something we don’t care about in the midst of an important meeting or class or date. The notification system becomes flexible and customizable enough to t the user’s life, not force the user to bend to their phone. </p>
        <p>Notifoo has two main sections. The main part of interface consists of completely customizable Lists. Users are able to create difierent lists of apps and contacts organized by category (e.i. social media, games, work, etc). For each list, the user is able to set custom delivery times for every day of the week. Notifoo collects and stores all notifications from apps and contacts on these lists and delivers them in bulk at the allotted times. With this system, a user is able to schedule all notifications from social media, for example, to be held until their lunch break during the work week, and then have them delivered in real time on the weekends.</p>
      </div>

      <div class="image-cont">
        <div class="image-1">
          <img src="img/projects/sleeping-bees/behancemockup-09.png" />
