      <div class="paragraph">
        <h2>Solution 2: Good Morning</h2>
        <p>At the same time, I had begun work on my second potential solution, Good Morning. This app was born from a problem that plagued me and many others that I spoke to. </p>
        <p>Every morning I turn off the alarm on my phone, and promptly check all of the notifications I’ve received while I was sleeping. The vast majority of the time, these notifications are not pressing in the least and do not deliver any information that would affect my morning routine. When I finish the list, I often feel disappointed, not just in the lack of interesting or relevant messages, but in myself for insisting on making this my first task of the day, instead of doing something pleasant and meaningful, like exercising, enjoying a cup of tea, or reading a bit of a good book.</p>
      </div>

      <div class="image-cont">
        <div class="image-1">
          <img src="img/projects/sleeping-bees/behancemockup-12.png" />
        </div>
      </div>

      <div class="paragraph">
        <p>is dilemma inspired Good Morning as a way to interrupt the interface flow that encourages checking notifications immediately upon opening the phone. Good Morning takes inspiration from Flipboard’s content customization system and Mailbox’s inbox clearing interface. Including basic information like the date and weather, Good Morning supplies something relaxing and pleasant, with an interface that encourages no further use of the phone once it’s been opened. Good Morning syncs with the user’s alarm clock. When the alarm goes off, Good Morning pushes a notification with daily content that is curated especially for each user. Each morning users will receive a single photograph, audio clip, or video with content that will relax them and put a positive spin on their morning. </p>
      </div>

