      <h1>Research</h1>

      <div class="paragraph">
        <p>As an alumni, I was already somewhat familiar with the difficulties of the original website but didn't learn the extent of it until commencing an extensive research phase. With the help of my the team at the library, I conducted interviews with members of each group of stakeholders and surveyed the student body. </p>
      </div>

      <div class="paragraph">
      <h2>Survey</h2>
        <p>The survey was sent out to the entire SUNY New Paltz student body. The first portion of questions collected demographic information, like enrollment status, major, and what types of classes they were taking. The remaining questions were carefully designed to gather quantifiable information on how students actually used the website without skewing responses, and with a few open ended questions that allowed students to share thoughts and requests.</p>
        <p>The responses were organized and cross-compared to find the most-used features, trends in usage, and statistics on how successful students are in using various features and what usage tendencies aligned to most successful users. This knowledge helped me understand what features needed to be prioritized and what needed to be improved.</p>
      </div>

      <div class="paragraph">
      <h2>Interviews</h2>
        <p>To round out the quantitative data with qualitative data, I interviewed members of each group of stakeholders to understand their priorities and what is causing problems in usability.</p>
        <p>I conducted short 1-3 minute interviews with students across campus, which, along with the open-ended survey questions, helped me understand common issues and get first hand-impressions of how students felt about the website.</p>
        <p>I conducted 10-15 minute interviews with campus faculty staff, including professors from various departments and staff from advising, records and registration, the tutoring center, and other departments.</p>
        <p>I conducted 30-45 minute group interview sessions with faculty and staff from each area of the library to fully understand how the website is used internally.</p>
        <p>The interview data was organized and categorized to find trends that weren't able to be detected by the survey and get a deeper understanding of daily usage for each group of users.</p>
      </div>

      <div class="paragraph">
      <h2>Conclusions</h2>
        <p>After completing the analyses and discovering results, I met with the web redesign team to discuss and make a list of realistic actions to take in designing the new website. I then presented the research methods, results, and action items to the entire library faculty, concluding the research phase.</p>
      </div>

